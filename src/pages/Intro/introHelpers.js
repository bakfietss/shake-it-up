// timer hulpfunctie
export function scheduleTimers(entries) {
  const timerIds = []

  for (const entry of entries) {
    const id = setTimeout(entry.fn, entry.delay)
    timerIds.push(id)
  }

  return function stopAlles() {
    for (const id of timerIds) {
      clearTimeout(id)
    }
  }
}
